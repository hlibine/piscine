int	ft_strcmp(char *s1, char *s2)
{
	int	c;

	c = 0;
	while (s1[c] != '\0' || s2[c] != '\0')
	{
		if (s1[c] == s2[c])
			c++;
		else
		{
			return (s1[c] - s2[c]);
		}
	}
	if (s1[0] == '\0')
		return (s1[0] - s2[c]);
	else if (s2[0] == '\0')
		return (s1[c] - s2[0]);
	return (0);
}
